-- Sample Queries
-- 1. View all employees with departments
SELECT E.Emp_ID, E.Emp_Name, E.Designation, D.Department_Name, E.Email
FROM Employee E JOIN Department D ON E.Dept_ID = D.Dept_ID;

-- 2. Attendance summary per employee (total present days)
SELECT E.Emp_ID, E.Emp_Name,
SUM(CASE WHEN A.Status='Present' THEN 1 ELSE 0 END) AS Total_Present,
SUM(CASE WHEN A.Status='Absent' THEN 1 ELSE 0 END) AS Total_Absent,
SUM(CASE WHEN A.Status='Leave' THEN 1 ELSE 0 END) AS Total_Leave
FROM Employee E
LEFT JOIN Attendance A ON E.Emp_ID = A.Emp_ID
GROUP BY E.Emp_ID, E.Emp_Name
ORDER BY Total_Present DESC;

-- 3. Daily attendance summary
SELECT Attendance_Date,
SUM(CASE WHEN Status='Present' THEN 1 ELSE 0 END) AS Present_Count,
SUM(CASE WHEN Status='Absent' THEN 1 ELSE 0 END) AS Absent_Count,
SUM(CASE WHEN Status='Leave' THEN 1 ELSE 0 END) AS Leave_Count
FROM Attendance
GROUP BY Attendance_Date
ORDER BY Attendance_Date;

-- 4. Employees with >3 absences in the period
SELECT E.Emp_ID, E.Emp_Name, COUNT(*) AS Absences
FROM Attendance A
JOIN Employee E ON A.Emp_ID = E.Emp_ID
WHERE A.Status='Absent'
GROUP BY E.Emp_ID, E.Emp_Name
HAVING COUNT(*) > 3
ORDER BY Absences DESC;
